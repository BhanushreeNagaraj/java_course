package java_progs;

public class loops {

	public static void main(String[] args) {
		int i=100;
		while(i<=105) {
			System.out.println(i);
			i++;
		}
	int j=1;
	do {
		System.out.println(j);
		j++;
	}while(j<=5);
	
	int k=1;
	int n=50;
	do {
		System.out.println(k);
		k++;
	}while(k<=n);
	
	int l,m=50;
	for(l=1;l<=50;l=l+2) {
		System.out.println(l);
	}
	
	int p;
	for(p=50;p>=0;p--) {
		System.out.println(p);
	}
	}

	}



